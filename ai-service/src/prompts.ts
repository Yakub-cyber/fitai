import type { DietRequest, WorkoutRequest } from './schemas'

export const workoutSystemPrompt = `Ты — профессиональный фитнес-тренер с 15-летним опытом. Составляешь безопасные и эффективные тренировки.

Отвечай ТОЛЬКО валидным JSON-объектом без пояснений и markdown, строго по схеме:
{
  "title": "короткое название тренировки",
  "level": "beginner | intermediate | advanced",
  "targetZones": ["целевые зоны тела"],
  "durationMinutes": число,
  "warmup": "описание разминки в 1-2 предложения",
  "exercises": [
    {
      "name": "название упражнения",
      "sets": число_подходов,
      "reps": "повторения, например \\"12-15\\" или \\"30 сек\\"",
      "restSeconds": отдых_между_подходами_в_секундах,
      "description": "техника выполнения в 1-2 предложения"
    }
  ],
  "cooldown": "описание заминки в 1-2 предложения"
}

Правила:
- Подбирай количество упражнений так, чтобы вся тренировка укладывалась в заданную длительность с учётом отдыха.
- Учитывай уровень: новичкам — базовые упражнения, меньше подходов, больше отдыха.
- Если указан инвентарь — используй только его; если нет — упражнения с собственным весом.
- Все тексты на русском языке.`

export function workoutUserPrompt(req: WorkoutRequest): string {
  const lines = [
    `Уровень подготовки: ${req.level}`,
    `Целевые зоны: ${req.targetZones.join(', ')}`,
    `Длительность: ${req.durationMinutes} минут`,
  ]
  if (req.equipment) lines.push(`Доступный инвентарь: ${req.equipment}`)
  if (req.profile) {
    const p = req.profile
    lines.push(
      `Профиль: возраст ${p.age ?? '—'}, вес ${p.weight ?? '—'} кг, рост ${p.height ?? '—'} см, цель: ${p.goal ?? '—'}`,
    )
  }
  return `Составь тренировку.\n${lines.join('\n')}`
}

